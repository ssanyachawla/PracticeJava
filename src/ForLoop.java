public class ForLoop {
    public static void main(String[] args) {
        //For Loop: Print numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println("i = " + i);
        }
        //
        int j=6;
        while(j<=10){
            System.out.println("While Loop: " + j);
            j++;
        }
    }
}