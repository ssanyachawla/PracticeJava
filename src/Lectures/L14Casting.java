package Lectures;


public class L14Casting {
    public static void main(String[] args) {
//        Implicit Casting
//        byte>short>int>long

        short x=1;
        int y=x+2;
        float s=2.2F;
        double t=s+1.1;
        System.out.println(y);
        System.out.println(t);

    }


}
